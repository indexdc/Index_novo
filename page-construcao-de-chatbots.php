              <p>Ao criar um chatbot, os desenvolvedores desse tipo de tecnologia cadastram na base de dados do programa, uma série de perguntas que podem ser feitas pelos usuários que entram em contato com uma empresa via chat.</p>
              <p>Um hotel, por exemplo, pode cadastrar respostas para perguntas sobre o preço da diária, os tipos de apartamento disponíveis, se há café da manhã incluso etc.</p>
              <p>A ideia é que os robôs respondam a todas essas perguntas automaticamente, sem que um atendente humano tenha que desperdiçar o seu tempo respondendo a questionamentos simples e repetitivos.</p>

